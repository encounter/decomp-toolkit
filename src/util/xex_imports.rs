#[inline]
fn from_lut(lut: &[&'static str], ordinal: usize) -> Option<String> {
    lut.get(ordinal).filter(|s| !s.contains("Unused")).map(|s| s.to_string())
}

fn table_for(lib_name: &str) -> Option<&'static [&'static str]> {
    if lib_name.contains("createprofile") {
        Some(&CREATEPROFILE_FUNCS)
    } else if lib_name.contains("connectx") {
        Some(&CONNECTX_FUNCS)
    } else if lib_name.contains("syscall") {
        Some(&SYSCALL_FUNCS)
    } else if lib_name.contains("vk") {
        Some(&VK_FUNCS)
    } else if lib_name.contains("xam") {
        Some(&XAM_FUNCS)
    } else if lib_name.contains("xapi") {
        Some(&XAPI_FUNCS)
    } else if lib_name.contains("xbdm") {
        Some(&XBDM_FUNCS)
    } else if lib_name.contains("xboxkrnl") {
        Some(&XBOXKRNL_FUNCS)
    } else {
        None
    }
}

pub fn replace_ordinal(lib_name: &String, ordinal: usize) -> String {
    if let Some(table) = table_for(lib_name) {
        if let Some(name) = from_lut(table, ordinal) {
            return name;
        }
    }

    log::warn!("import library {} ordinal {:04X} not found!", lib_name, ordinal);
    format!("{:04X}", ordinal)
}

